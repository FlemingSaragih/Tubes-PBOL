    public boolean val() {
        return !nama.getText().isEmpty() && !hp.getText().isEmpty() && !dosen.getText().isEmpty() && !status.getText().isEmpty() && !kelas.getText().isEmpty() && !tp.getText().isEmpty() && !jumlah.getText().isEmpty() && !instruktur.getText().isEmpty();
    } 
    public void save() {
        try {
            if (val()) {
                gami1 a = new gami1();
                a.setnama(nama.getText());
                a.sethp(Integer.valueOf(hp.getText()));
                a.setdosen(dosen.getText());
                a.setjk(String.valueOf(jk.getSelectedItem()));
                a.setstatus(status.getText());
                a.setkelas(kelas.getText());
                a.settp(Integer.valueOf(tp.getText()));
                a.setjumlah(Integer.valueOf(jumlah.getText()));
                a.setinstruktur(instruktur.getText());
                a.sethari(String.valueOf(hari.getSelectedItem()));
                a.setwaktu(String.valueOf(waktu.getSelectedItem()));
                if (tag == 0) {
                    service.insert(a);
                    //totalr.setText(String.valueOf(getTotal()));
                } else if (tag == 1) {
                    index = jTable1.getSelectedRow();
                    service.update(index, a);
                }
                reset();
                this.view();
            } else {
                JOptionPane.showMessageDialog(null, "Pastikan Data Telah Terisi dengan Lengkap !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Kesalahan Pada " + e);
        }
    }
